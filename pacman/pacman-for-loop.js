//pacman-for-loop.js
var ghosts = ['Inky', 'Blinky', 'Pinky', 'Clyde'];

for (var index = 0; index < ghosts.length; index++) {
  console.log('Pac-Man eats ' + ghosts[index]);
}

// index ++ is for increment increase of 1
// index -- for decrement reduction by 1
